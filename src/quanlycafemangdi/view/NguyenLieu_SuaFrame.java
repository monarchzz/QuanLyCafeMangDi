    public void thietLapDuLieu(NguyenLieu nguyenLieu)
    {
        this.nguyenLieu = nguyenLieu;
        
        maNguyenLieu_TF.setText(nguyenLieu.getMaNguyenLieu());
        tenNguyenLieu_TF.setText(nguyenLieu.getTenNguyenLieu());
        donViTinh_Cbx.setSelectedItem(nguyenLieu.getDonViTinh());
        gia_TF.setText(nguyenLieu.getGia() + "");
    }    
    
    public void layDonViTinh()
    {
        Connection connect = Util.getConnection();
        String query = "select * from DonViTinh";
        try
        {
            PreparedStatement ps = connect.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next())
            {
                donViTinh_Cbx.addItem(rs.getString("tenDV"));
            }
        }catch (SQLException ex)
        {
            System.out.println("Lay don vi tinh len combo box that bai");
        }
    }
    
    public String layMaDonVi(String donViTinh)
    {
        Connection connect = Util.getConnection();
        String query = "select * from DonViTinh where tenDV = '" + donViTinh + "'";
        try
        {
            PreparedStatement ps = connect.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            if (rs.next())
            {
                return rs.getString("maDV");
            }
        }catch (SQLException ex)
        {
            System.out.println("Lay ma don vi that bai");
        }
        return null;
    }
    
    public boolean kiemTraLoi()
    {   
        loi = "";
       
        String tenNguyenLieu = tenNguyenLieu_TF.getText();
        String gia = gia_TF.getText();
        
        if (tenNguyenLieu.equals(""))
        {
            loi = loi + "Tên nguyên liệu không được để trống\n";
        }

        if (gia.equals(""))
        {
            loi = loi + "Giá tiền không được để trống\n";
        }
        else if (gia.matches("\\d+") == false)
        {
            loi = loi + "Giá tiền chỉ có thể là số\n";
        }
        
        if (loi.equals("") == false)
        {
            hienThiLoi(loi);
            return true;
        }
        else
        {
            return false;
        }
    }    
    
    public void hienThiLoi(String loi)
    {
        JOptionPane.showMessageDialog(rootPane, loi, "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
       
    public void suaNguyenLieu()
    {
        String tenNguyenLieu = tenNguyenLieu_TF.getText();
        String donViTinh = (String) donViTinh_Cbx.getSelectedItem();
        String maDonVi = layMaDonVi(donViTinh);
        String gia = gia_TF.getText();
        
        nguyenLieu.setTenNguyenLieu(tenNguyenLieu);
        nguyenLieu.setMaDonVi(maDonVi);
        nguyenLieu.setDonViTinh(donViTinh);
        nguyenLieu.setGia(Integer.parseInt(gia));
        
        suaNguyenLieuSQL(nguyenLieu);
        JOptionPane.showMessageDialog(rootPane, "Sửa thông tin nguyên liệu thành công");     
        this.dispose();
    }
    
    public void suaNguyenLieuSQL(NguyenLieu nguyenLieu)
    {
        Connection connect = Util.getConnection();
        String query = "update NguyenLieu set tenNL = ?, maDV = ?, gia = ? where maNL = '" 
                + nguyenLieu.getMaNguyenLieu() + "'";
        try
        {
            PreparedStatement ps = connect.prepareStatement(query);
            ps.setString(1, nguyenLieu.getTenNguyenLieu());
            ps.setString(2, nguyenLieu.getMaDonVi());
            ps.setLong(3, nguyenLieu.getGia());
            ps.executeUpdate();
            ps.close();
            connect.close();
        }catch (SQLException ex)
        {
            System.out.println("Sua nguyen lieu that bai");
        }
    }

